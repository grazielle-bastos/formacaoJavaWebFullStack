package cursoJava.executavel;

public class OperacaoInt {
	
	public static void main(String[] args) {
		
		/* - Operação entre números inteiros */
		
		int nota1 = 90;
		int nota2 = 70;
		int nota3 = 50;
		int nota4 = 88;
		
		System.out.println("Média das notas = " + (nota1 + nota2 + nota3 + nota4) / 4);		
	}

}
