package cursoJava.operacoes;

public class OperadoresRelacionais {
	
	public static void main(String[] args) {
		
		/*
		 * Operadores relacionais: Finalidade de Comparação
		 * 
		 * Operador == Comparação de igual
		 * Operador != Comparação de diferente
		 * Operador < Comparação de menor
		 * Operador > Comparação de maior
		 * Operador <= Comparação de menor ou igual
		 * Operador >= Comparação de maior ou igual
		*/
		
		int nota1 = 90;
		int nota2 = 91;
		
		if (nota1 != nota2) {
			System.out.println("As notas são diferentes");
		} else {
			System.out.println("As notas são iguais");
		}
		
	}

}
